
public class EjercicioTablaMultiplicacion {
	public static void main(String[] args) {
		for (int contador = 1; contador <= 10; contador++) {
			for (int multiplica = 1; multiplica <= 10; multiplica++) {
				System.out.print(contador * multiplica);
				System.out.print(" ");
			}
			System.out.println();
		}
	}
}
